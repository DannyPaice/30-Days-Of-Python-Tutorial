
import numpy as np
# Mathematical Operations using numpy
numpy_array_from_list = np.array([1,2,3,4,5])

# Addition
print('original array: ', numpy_array_from_list)
ten_plus_original = numpy_array_from_list + 10
print('added array: ', ten_plus_original)


# Subtraction
ten_minus_original = numpy_array_from_list - 10
print('subtracted array: ', ten_minus_original)


# Multiplication
ten_times_original = numpy_array_from_list * 10
print('multiplied array: ', ten_times_original)


# Division
ten_divided_original = numpy_array_from_list / 10
print('divided array: ', ten_divided_original)


# Modulus
modulus_3_original = numpy_array_from_list % 3
print('modulus 3 original: ', modulus_3_original)


# Floor Division
ten_floor_original = numpy_array_from_list // 10
print('Floor by 10 array: ', ten_floor_original)


# Exponential
squared_original = numpy_array_from_list ** 2
print('squared array: ', squared_original)





